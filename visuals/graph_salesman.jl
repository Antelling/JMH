using Luxor

function graph(system::Vector{<:Real})
    if length(system) == 500
        data = big_cities
        scale = 2
    else
        data = small_cities
        scale = 19
    end
    Drawing(1000, 1000, "visuals/$(score(system, data)).png")
    background("white")
    sethue("black")

    for i in 1:length(system)-1
        x1, y1 = data[system[i]]
        x2, y2 = data[system[i+1]]
        line(Point(x1*scale, y1*scale), Point(x2*scale, y2*scale), :stroke)
    end

    x1, y1 = data[system[1]]
    x2, y2 = data[system[end]]
    line(Point(x1*scale, y1*scale), Point(x2*scale, y2*scale), :stroke)

    finish()
    #preview()
end

